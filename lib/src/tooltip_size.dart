class TooltipSize {
  final double? height;
  final double? width;
  final double spacing;
  final double horizontalPadding;
  final double verticalPadding;

  const TooltipSize({
    this.height,
    this.width,
    this.spacing = 10,
    this.horizontalPadding = 0,
    this.verticalPadding = 0,
  });
}
